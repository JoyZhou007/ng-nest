/**
 * 组件中的样式参数对象
 *
 * @export
 * @interface NcStyle
 */
export interface NcStyle {
  /**
   * 属性名
   *
   * @type {string}
   * @memberof NcStyle
   */
  name?: string;
  /**
   * 名称
   *
   * @type {string}
   * @memberof NcStyle
   */
  label?: string;
  /**
   * 描述
   *
   * @type {string}
   * @memberof NcStyle
   */
  description?: string;
  /**
   * 值
   *
   * @type {string}
   * @memberof NcStyle
   */
  value?: string;
  /**
   * 实际值
   *
   * @type {string}
   * @memberof NcStyle
   */
  inherit?: string;
  /**
   * 继承样式参数对象
   *
   * @type {string}
   * @memberof NcStyle
   */
  children?: NcStyle[];
}
